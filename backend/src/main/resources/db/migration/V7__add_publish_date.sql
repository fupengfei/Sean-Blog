-- =============================================================================
-- V7: 文章表增加自定义发布日期字段
-- =============================================================================
-- 变更原因：发布日期的展示可能与实际创建时间不同（如补发历史文章）
-- publish_date 为可选字段，NULL 时可回退使用 created_at 作为展示日期
-- =============================================================================

ALTER TABLE t_article ADD COLUMN publish_date DATE DEFAULT NULL COMMENT '用户自定义发布日期，页面展示用' AFTER author;
